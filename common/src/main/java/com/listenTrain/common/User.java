package com.listenTrain.common;

public class User {

	String name;
	String pwdMd5;
	String state;
	
	String id;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPwdMd5() {
		return pwdMd5;
	}

	public void setPwdMd5(String pwdMd5) {
		this.pwdMd5 = pwdMd5;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	
	
}
